     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request->input());
        $this->validate($request, [
            'name' => 'required',
            'email' => 'required|email',
            'profile_id' => 'required',
            'password' => 'required',
        ]);

        User::create($request->all());
        return redirect('users')->with([
            'msg' => 'Usuario Creado',
            'msgType' => 'success'
        ]);
    }

